package lesson_01.homework_01;

public class Homework_01 {
    public static void main(String[] args) {
        System.out.println("Hello world!");

        int a = 5; // 32 - битное целое число (также есть тоже самое другой битности bite, short, long)
        System.out.println(a);
        double b = 7.2;  // 64 - битное значение с плавающей точкой (также есть 32 - битное float
        System.out.println(b);
        boolean c = a < b; // истинное или ложное значение
        System.out.println(c);
        char d = 'S'; // 16 - битное значение в системе unicode
        System.out.println(d);

    }
}
